package plugin.ui.window.configuration.confighandlers.Static;

public class MetricData {
	String name;
	int level;
	String rangeRegPattern;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLevel() {
		return level;
	}
	public void setLevel(int level) {
		this.level = level;
	}
	public String getRangeRegPattern() {
		return rangeRegPattern;
	}
	public void setRangeRegPattern(String rangeRegPattern) {
		this.rangeRegPattern = rangeRegPattern;
	}
}
